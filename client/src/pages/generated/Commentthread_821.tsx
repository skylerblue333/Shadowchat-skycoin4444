// Define types for comments and authors
interface Comment {
  id: string;
  author: {
    name: string;
    avatarUrl: string;
  };
  content: string;
  timestamp: string;
}

// Mock tRPC context and hooks for demonstration purposes
// In a real application, these would be generated by tRPC and connected to a backend

interface CommentThreadProps {
  postId: string;
}

const CommentThread: React.FC<any> = ({ postId }) => {
  const [newCommentContent, setNewCommentContent] = React.useState<string>('');

  const { data: comments, isLoading: isLoadingComments, error: commentsError } = useStubQuery({ postId });
  const { mutate: addComment, isLoading: isLoadingPost, error: addCommentError } = useStubMutation();

  const handleSubmit = async () => {
    if (newCommentContent.trim()) {
      try {
        // In a real app, you would typically refetch comments or update the cache
        // after a successful post. For this mock, we just clear the input.
        await addComment({ postId, content: newCommentContent });
        setNewCommentContent('');
      } catch (e) {
        // Error is already handled by the mock tRPC hook, no need to re-throw or log here
      }
    }
  };

  const displayError = commentsError?.message || addCommentError?.message || null;

  return (
    <div className="container mx-auto p-4 max-w-2xl dark:bg-gray-900 dark:text-gray-100 min-h-screen">
      <Card className="mb-4 dark:bg-gray-800 dark:border-gray-700">
        <CardHeader className="font-bold text-lg dark:text-gray-50">Comments</CardHeader>
        <CardContent>
          <div className="flex items-start space-x-4 mb-4">
            <Avatar>
              <AvatarImage src="https://github.com/shadcn.png" alt="Current user avatar" />
              <AvatarFallback>CU</AvatarFallback>
            </Avatar>
            <div className="flex-1">
              <Textarea
                placeholder="Write a comment..."
                value={newCommentContent}
                onChange={(e) => setNewCommentContent(e.target.value)}
                rows={3}
                aria-label="Write a new comment"
                className="dark:bg-gray-700 dark:text-gray-50 dark:border-gray-600"
              />
              <Button
                onClick={handleSubmit}
                className="mt-2 dark:bg-blue-600 dark:hover:bg-blue-700 dark:text-white"
                disabled={isLoadingPost || !newCommentContent.trim()}
              >
                {isLoadingPost ? 'Posting...' : 'Post Comment'}
              </Button>
            </div>
          </div>
          {displayError && <p className="text-red-500 text-sm mb-4" role="alert">Error: {displayError}</p>}

          <div className="space-y-4">
            {isLoadingComments && <p className="text-center text-gray-500 dark:text-gray-400">Loading comments...</p>}
            {!isLoadingComments && comments?.length === 0 && (
              <p className="text-center text-gray-500 dark:text-gray-400">No comments yet. Be the first to comment!</p>
            )}
            {!isLoadingComments && comments && comments.map((comment) => (
              <div key={comment.id} className="flex items-start space-x-4">
                <Avatar>
                  <AvatarImage src={comment.author.avatarUrl} alt={`${comment.author.name}'s avatar`} />
                  <AvatarFallback>{comment.author.name.substring(0, 2)}</AvatarFallback>
                </Avatar>
                <div className="flex-1 bg-gray-100 dark:bg-gray-700 p-3 rounded-lg">
                  <div className="flex justify-between items-center mb-1">
                    <span className="font-semibold dark:text-gray-50">{comment.author.name}</span>
                    <span className="text-xs text-gray-500 dark:text-gray-400">{comment.timestamp}</span>
                  </div>
                  <p className="text-sm dark:text-gray-200">{comment.content}</p>
                </div>
              </div>
            ))}
          </div>
        </CardContent>
      </Card>
    </div>
  );
};

export default CommentThread;
