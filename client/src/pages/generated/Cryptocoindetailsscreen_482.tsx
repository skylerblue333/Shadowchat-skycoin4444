// Mock tRPC hook for fetching coin details
// In a real application, this would be generated by tRPC
const useCoinDetailsQuery = (coinId: string) => {
  return useQuery<CoinDetails, Error>({
    queryKey: ['coinDetails', coinId],
    queryFn: async () => {
      // Simulate API call
      const response = await fetch(`https://api.coincap.io/v2/assets/${coinId}`);
      if (!response.ok) {
        throw new Error(`Failed to fetch coin details for ${coinId}`);
      }
      const data = await response.json();
      return data.data;
    },
    enabled: !!coinId,
  });
};

