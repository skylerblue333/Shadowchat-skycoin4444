const SearchHistoryScreen: React.FC = () => {
  const [searchQuery, setSearchQuery] = useState('');
  const [isDarkMode, setIsDarkMode] = useState(false);
  const { data: searchHistory, isLoading, isError, error } = useSearchHistory();

  const filteredHistory = searchHistory?.filter(item =>
    item.query.toLowerCase().includes(searchQuery.toLowerCase())
  );

  const handleSearchChange = useCallback((e: React.ChangeEvent<HTMLInputElement>) => {
    setSearchQuery(e.target.value);
  }, []);

  const toggleDarkMode = useCallback(() => {
    setIsDarkMode(prevMode => !prevMode);
    document.documentElement.classList.toggle('dark', !isDarkMode);
  }, [isDarkMode]);

  return (
    <div className={`min-h-screen p-8 ${isDarkMode ? 'dark bg-gray-900 text-white' : 'bg-gray-50 text-gray-900'}`}>
      <div className="max-w-3xl mx-auto space-y-6">
        <div className="flex justify-between items-center">
          <h1 className="text-4xl font-bold flex items-center gap-2">
            <HistoryIcon className="w-8 h-8" /> Search History
          </h1>
          <div className="flex items-center space-x-2">
            <Switch
              id="dark-mode-toggle"
              checked={isDarkMode}
              onCheckedChange={toggleDarkMode}
              aria-label="Toggle dark mode"
            />
            <Label htmlFor="dark-mode-toggle">
              {isDarkMode ? <MoonIcon className="w-5 h-5" /> : <SunIcon className="w-5 h-5" />}
            </Label>
          </div>
        </div>

        <div className="relative">
          <SearchIcon className="absolute left-3 top-1/2 -translate-y-1/2 w-5 h-5 text-gray-400" />
          <Input
            type="text"
            placeholder="Filter search history..."
            value={searchQuery}
            onChange={handleSearchChange}
            className="pl-10 pr-4 py-2 w-full rounded-md border border-gray-300 focus:ring-2 focus:ring-blue-500 focus:border-transparent dark:bg-gray-800 dark:border-gray-700 dark:text-white"
            aria-label="Filter search history"
          />
        </div>

        {isLoading && (
          <div className="space-y-4">
            {[...Array(4)].map((_, i) => (
              <Card key={i} className="dark:bg-gray-800 dark:border-gray-700">
                <CardHeader>
                  <Skeleton className="h-6 w-3/4 mb-2 dark:bg-gray-700" />
                  <Skeleton className="h-4 w-1/2 dark:bg-gray-700" />
                </CardHeader>
              </Card>
            ))}
          </div>
        )}

        {isError && (
          <Card className="border-red-500 bg-red-50 dark:bg-red-950 dark:border-red-700">
            <CardHeader>
              <CardTitle className="text-red-700 dark:text-red-300 flex items-center gap-2">
                <XCircleIcon className="w-5 h-5" /> Error Loading History
              </CardTitle>
              <CardDescription className="text-red-600 dark:text-red-400">
                {error?.message || 'Failed to fetch search history. Please try again later.'}
              </CardDescription>
            </CardHeader>
          </Card>
        )}

        {!isLoading && !isError && filteredHistory && filteredHistory.length === 0 && (
          <Card className="dark:bg-gray-800 dark:border-gray-700">
            <CardHeader>
              <CardTitle className="text-gray-700 dark:text-gray-300">No History Found</CardTitle>
              <CardDescription className="text-gray-600 dark:text-gray-400">
                Your search history is empty or no results match your filter.
              </CardDescription>
            </CardHeader>
          </Card>
        )}

        {!isLoading && !isError && filteredHistory && filteredHistory.length > 0 && (
          <div className="space-y-4">
            {filteredHistory.map(item => (
              <Card key={item.id} className="dark:bg-gray-800 dark:border-gray-700">
                <CardHeader>
                  <CardTitle className="text-lg dark:text-white">{item.query}</CardTitle>
                  <CardDescription className="dark:text-gray-400">
                    Searched on {new Date(item.timestamp).toLocaleString()}
                  </CardDescription>
                </CardHeader>
                <CardContent>
                  <Button variant="outline" className="dark:text-white dark:border-gray-600 dark:hover:bg-gray-700">
                    View Details
                  </Button>
                </CardContent>
              </Card>
            ))}
          </div>
        )}
      </div>
    </div>
  );
};

export default SearchHistoryScreen;
