// Placeholder for tRPC hook. In a real app, this would be generated by tRPC.
// For demonstration, we'll simulate a tRPC call.

interface SearchPaginationProps {
  initialPage?: number;
  initialSearchQuery?: string;
}

const SearchPagination: React.FC<any> = ({
  initialPage = 1,
  initialSearchQuery = '',
}) => {
  const [currentPage, setCurrentPage] = useState(initialPage);
  const [searchQuery, setSearchQuery] = useState(initialSearchQuery);
  const [currentSearchTerm, setCurrentSearchTerm] = useState(initialSearchQuery);
  const { toast } = useToast();

  const { data, isLoading, isError } = useStubQuery({
    page: currentPage,
    searchTerm: currentSearchTerm,
  });

  const totalPages = data?.totalPages || 1;

  useEffect(() => {
    if (isError) {
      toast({
        title: 'Search Error',
        description: 'Failed to fetch search results. Please try again.',
        variant: 'destructive',
      });
    }
  }, [isError, toast]);

  const handlePageChange = useCallback((page: number) => {
    setCurrentPage(page);
  }, []);

  const handleSearchSubmit = useCallback((e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    if (!searchQuery.trim()) {
      toast({
        title: 'Empty Search',
        description: 'Please enter a search term.',
        variant: 'warning',
      });
      return;
    }
    setCurrentPage(1); // Reset to first page on new search
    setCurrentSearchTerm(searchQuery);
  }, [searchQuery, toast]);

  const handlePrevious = useCallback(() => {
    if (currentPage > 1) {
      handlePageChange(currentPage - 1);
    }
  }, [currentPage, handlePageChange]);

  const handleNext = useCallback(() => {
    if (currentPage < totalPages) {
      handlePageChange(currentPage + 1);
    }
  }, [currentPage, totalPages, handlePageChange]);

  return (
    <div className="flex flex-col items-center space-y-6 p-6 bg-white dark:bg-gray-900 text-gray-900 dark:text-white rounded-lg shadow-lg max-w-2xl mx-auto">
      <h2 className="text-2xl font-bold mb-4" id="search-pagination-heading">Search Results Pagination</h2>
      
      <form onSubmit={handleSearchSubmit} className="flex w-full max-w-md items-center space-x-3" role="search" aria-labelledby="search-pagination-heading">
        <Input
          type="search"
          placeholder="Enter search term..."
          value={searchQuery}
          onChange={(e) => setSearchQuery(e.target.value)}
          className="flex-grow dark:bg-gray-800 dark:border-gray-700 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent"
          aria-label="Search input"
          disabled={isLoading}
        />
        <Button type="submit" className="dark:bg-blue-600 dark:hover:bg-blue-700 dark:text-white transition-colors duration-200" disabled={isLoading}>
          {isLoading ? <Loader2 className="mr-2 h-4 w-4 animate-spin" /> : <SearchIcon className="mr-2 h-4 w-4" />}
          Search
        </Button>
      </form>

      {isLoading && (
        <div className="flex items-center justify-center text-blue-500" role="status" aria-live="polite">
          <Loader2 className="mr-2 h-5 w-5 animate-spin" />
          <span>Loading results...</span>
        </div>
      )}

      {isError && !isLoading && (
        <p className="text-red-500 text-sm" role="alert">Error loading search results. Please try again.</p>
      )}

      {!isLoading && !isError && totalPages === 0 && currentSearchTerm && (
        <p className="text-gray-500 dark:text-gray-400" role="status">No results found for "{currentSearchTerm}".</p>
      )}

      {!isLoading && !isError && totalPages > 0 && (
        <div className="flex items-center space-x-4" role="navigation" aria-label="Pagination">
          <Button
            variant="outline"
            size="icon"
            onClick={handlePrevious}
            disabled={currentPage === 1 || isLoading}
            className="dark:bg-gray-700 dark:border-gray-600 dark:hover:bg-gray-600 dark:text-white transition-colors duration-200"
            aria-label="Previous page"
          >
            <ChevronLeftIcon className="h-5 w-5" />
          </Button>
          <span className="text-md font-medium sr-only">Page</span>
          <Input
            type="number"
            value={currentPage}
            onChange={(e) => {
              const page = parseInt(e.target.value, 10);
              if (!isNaN(page) && page >= 1 && page <= totalPages) {
                handlePageChange(page);
              }
            }}
            className="w-16 text-center dark:bg-gray-800 dark:border-gray-700 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent"
            aria-label={`Current page, page ${currentPage} of ${totalPages}`}
            min={1}
            max={totalPages}
            disabled={isLoading}
          />
          <span className="text-md font-medium">of {totalPages}</span>
          <Button
            variant="outline"
            size="icon"
            onClick={handleNext}
            disabled={currentPage === totalPages || isLoading}
            className="dark:bg-gray-700 dark:border-gray-600 dark:hover:bg-gray-600 dark:text-white transition-colors duration-200"
            aria-label="Next page"
          >
            <ChevronRightIcon className="h-5 w-5" />
          </Button>
        </div>
      )}
    </div>
  );
};

export default SearchPagination;
