// Mock tRPC client for demonstration purposes
// In a real application, this would be generated by tRPC

interface StreamData {
  id: string;
  title: string;
  description: string;
  viewerCount: number;
  isLive: boolean;
  streamUrl: string;
}

const LiveStream: React.FC = () => {
  const [streamId, setStreamId] = useState<string>('skycoin-live-1');
  const [isDarkMode, setIsDarkMode] = useState<boolean>(false);

  const { data, isLoading, isError, error, refetch } = useStubQuery(streamId);

  useEffect(() => {
    if (isDarkMode) {
      document.documentElement.classList.add('dark');
    } else {
      document.documentElement.classList.remove('dark');
    }
  }, [isDarkMode]);

  if (isLoading) {
    return (
      <div className="flex items-center justify-center min-h-screen">
        <p className="text-lg">Loading live stream data...</p>
      </div>
    );
  }

  if (isError) {
    return (
      <div className="flex flex-col items-center justify-center min-h-screen text-red-500">
        <p className="text-lg">Error: {error?.message || 'Failed to load stream data.'}</p>
        <Button onClick={() => refetch()} className="mt-4">Retry</Button>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-background text-foreground p-4">
      <div className="flex justify-end mb-4">
        <div className="flex items-center space-x-2">
          <Switch
            id="dark-mode"
            checked={isDarkMode}
            onCheckedChange={setIsDarkMode}
            aria-label="Toggle dark mode"
          />
          <Label htmlFor="dark-mode">Dark Mode</Label>
        </div>
      </div>

      <Card className="w-full max-w-4xl mx-auto shadow-lg rounded-lg overflow-hidden">
        <CardHeader className="bg-primary text-primary-foreground p-4">
          <CardTitle className="text-2xl font-bold">{data?.title}</CardTitle>
        </CardHeader>
        <CardContent className="p-4">
          {data?.isLive ? (
            <div className="aspect-video w-full mb-4">
              <iframe
                className="w-full h-full rounded-md"
                src={data.streamUrl}
                frameBorder="0"
                allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture"
                allowFullScreen
                title="Live Stream"
                aria-label="Live Stream Video Player"
              ></iframe>
            </div>
          ) : (
            <div className="flex items-center justify-center h-64 bg-muted rounded-md mb-4">
              <p className="text-muted-foreground text-xl">Stream is currently offline.</p>
            </div>
          )}

          <p className="text-lg mb-2">{data?.description}</p>
          <p className="text-sm text-muted-foreground">Viewers: {data?.viewerCount}</p>

          <div className="mt-6 flex justify-center">
            <Button className="bg-accent text-accent-foreground hover:bg-accent/90">
              Join Chat
            </Button>
          </div>
        </CardContent>
      </Card>
    </div>
  );
};

export default LiveStream;
