interface EconomicEvent {
  id: string;
  name: string;
  date: string;
  time: string;
  impact: 'Low' | 'Medium' | 'High';
}

const getImpactColor = (impact: EconomicEvent['impact']) => {
  switch (impact) {
    case 'High':
      return 'text-red-500';
    case 'Medium':
      return 'text-yellow-500';
    case 'Low':
      return 'text-green-500';
    default:
      return 'text-gray-500';
  }
};

export const EconomicCalendar = () => {
  const { data: events, isLoading, isError, error } = useStubQuery();
  const [isDarkTheme, setIsDarkTheme] = React.useState(false);

  if (isError) {
    return <div className="p-4 text-red-600 dark:text-red-400" role="alert">Error: {error?.message}</div>;
  }

  return (
    <div className={`min-h-screen p-4 ${isDarkTheme ? 'dark bg-gray-900 text-white' : 'bg-gray-100 text-gray-900'}`} aria-live="polite">
      <div className="flex justify-end mb-4">
        <Label htmlFor="dark-mode-switch" className="mr-2">Dark Mode</Label>
        <Switch
          id="dark-mode-switch"
          checked={isDarkTheme}
          onCheckedChange={setIsDarkTheme}
          aria-label="Toggle dark mode"
        />
      </div>

      <Card className="w-full max-w-4xl mx-auto shadow-lg dark:bg-gray-800 dark:border-gray-700">
        <CardHeader>
          <CardTitle className="text-2xl font-bold dark:text-white">Crypto Economic Calendar</CardTitle>
        </CardHeader>
        <CardContent>
          {isLoading ? (
            <div className="space-y-4">
              {[...Array(4)].map((_, i) => (
                <Skeleton key={i} className="h-12 w-full dark:bg-gray-700" />
              ))}
            </div>
          ) : (
            <ul className="space-y-4" aria-label="Economic Events">
              {events?.map((event) => (
                <li key={event.id} className="flex flex-col sm:flex-row justify-between items-start sm:items-center p-3 border-b last:border-b-0 dark:border-gray-700">
                  <div className="flex-1">
                    <p className="font-semibold text-lg dark:text-white" aria-label={`Event name: ${event.name}`}>{event.name}</p>
                    <p className="text-sm text-gray-600 dark:text-gray-400" aria-label={`Date: ${event.date}, Time: ${event.time}`}>{event.date} at ${event.time}</p>
                  </div>
                  <div className={`font-bold ${getImpactColor(event.impact)}`} aria-label={`Impact level: ${event.impact}`}>
                    {event.impact} Impact
                  </div>
                </li>
              ))}
            </ul>
          )}
        </CardContent>
      </Card>
    </div>
  );
};

