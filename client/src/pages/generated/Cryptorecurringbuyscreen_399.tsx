const useCreateRecurringBuy = () => {
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [isSuccess, setIsSuccess] = useState(false);

  const mutate = useCallback(async (data: FormData) => {
    setIsLoading(true);
    setError(null);
    setIsSuccess(false);
    try {
      // Simulate API call
      await new Promise(resolve => setTimeout(resolve, 1500));
      console.log('Recurring buy created:', data);
      setIsSuccess(true);
      return { success: true };
    } catch (err) {
      setError('Failed to create recurring buy. Please try again.');
      return { success: false, error: 'Failed to create recurring buy' };
    } finally {
      setIsLoading(false);
    }
  }, []);

  return { mutate, isLoading, error, isSuccess };
};

