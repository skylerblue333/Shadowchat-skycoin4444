// Placeholder for tRPC hooks - in a real app, these would be generated by tRPC
// For demonstration, we'll simulate data fetching.
interface ConnectedApp {
  id: string;
  name: string;
  status: 'connected' | 'disconnected' | 'pending';
}

interface UseConnectedAppsResult {
  data: ConnectedApp[] | undefined;
  isLoading: boolean;
  isError: boolean;
  error: Error | null;
  refetch: () => void;
}

const useConnectedApps = (): UseConnectedAppsResult => {
  const [data, setData] = useState<ConnectedApp[] | undefined>(undefined);
  const [isLoading, setIsLoading] = useState(true);
  const [isError, setIsError] = useState(false);
  const [error, setError] = useState<Error | null>(null);

  const fetchData = () => {
    setIsLoading(true);
    setIsError(false);
    setError(null);
    // Simulate API call
    setTimeout(() => {
      if (Math.random() > 0.8) { // Simulate error 20% of the time
        setIsError(true);
        setError(new Error('Failed to fetch connected apps.'));
        setData(undefined);
      } else {
        setData([
          { id: '1', name: 'DeFi Wallet', status: 'connected' },
          { id: '2', name: 'NFT Marketplace', status: 'disconnected' },
          { id: '3', name: 'Exchange Platform', status: 'pending' },
        ]);
      }
      setIsLoading(false);
    }, 1500);
  };

  useEffect(() => {
    fetchData();
  }, []);

  return { data, isLoading, isError, error, refetch: fetchData };
};

const CryptoConnectedApps: React.FC = () => {
  const { data: connectedApps, isLoading, isError, error, refetch } = useConnectedApps();
  const [isDarkTheme, setIsDarkTheme] = useState(true); // Simulate dark theme toggle

  // Accessibility: Apply dark theme class to body or a wrapper element
  useEffect(() => {
    if (isDarkTheme) {
      document.documentElement.classList.add('dark');
    } else {
      document.documentElement.classList.remove('dark');
    }
  }, [isDarkTheme]);

  return (
    <div className="min-h-screen bg-background text-foreground p-4 dark:bg-gray-900 dark:text-gray-50">
      <Card className="w-full max-w-2xl mx-auto">
        <CardHeader>
          <CardTitle className="text-2xl font-bold">Connected Apps</CardTitle>
        </CardHeader>
        <CardContent>
          <div className="flex items-center justify-between mb-4">
            <Label htmlFor="dark-mode" className="text-lg">Dark Mode</Label>
            <Switch
              id="dark-mode"
              checked={isDarkTheme}
              onCheckedChange={setIsDarkTheme}
              aria-label="Toggle dark mode"
            />
          </div>

          <Button onClick={refetch} disabled={isLoading} className="mb-4">
            {isLoading ? <Loader2 className="mr-2 h-4 w-4 animate-spin" /> : null}
            Refresh Apps
          </Button>

          {isLoading && (
            <div className="flex items-center justify-center py-8">
              <Loader2 className="h-8 w-8 animate-spin text-primary" aria-label="Loading connected apps" />
              <span className="ml-2 text-lg">Loading connected apps...</span>
            </div>
          )}

          {isError && (
            <div className="flex items-center text-red-500 py-4" role="alert">
              <AlertCircle className="h-5 w-5 mr-2" />
              <span>Error: {error?.message || 'Failed to load apps.'} Please try again.</span>
            </div>
          )}

          {!isLoading && !isError && connectedApps && connectedApps.length > 0 ? (
            <ul className="space-y-4">
              {connectedApps.map((app) => (
                <li key={app.id} className="flex items-center justify-between p-3 border rounded-md bg-card-foreground/5">
                  <span className="font-medium text-lg">{app.name}</span>
                  <div className="flex items-center space-x-2">
                    {app.status === 'connected' && (
                      <span className="text-green-500 flex items-center">
                        <CheckCircle2 className="h-4 w-4 mr-1" /> Connected
                      </span>
                    )}
                    {app.status === 'disconnected' && (
                      <span className="text-red-500 flex items-center">
                        <AlertCircle className="h-4 w-4 mr-1" /> Disconnected
                      </span>
                    )}
                    {app.status === 'pending' && (
                      <span className="text-yellow-500 flex items-center">
                        <Loader2 className="h-4 w-4 mr-1 animate-spin" /> Pending
                      </span>
                    )}
                    <Button variant="outline" size="sm">
                      {app.status === 'connected' ? 'Disconnect' : 'Connect'}
                    </Button>
                  </div>
                </li>
              ))}
            </ul>
          ) : (!isLoading && !isError && connectedApps?.length === 0) ? (
            <p className="text-center text-muted-foreground py-8">No connected apps found.</p>
          ) : null}
        </CardContent>
      </Card>
    </div>
  );
};

export default CryptoConnectedApps;
