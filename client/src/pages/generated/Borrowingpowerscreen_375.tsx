// Assume these types are generated by tRPC or defined elsewhere
interface BorrowingPowerData {
  totalCollateral: number;
  totalBorrowed: number;
  borrowLimit: number;
  availableBorrowPower: number;
  healthFactor: number;
}

interface ErrorResponse {
  message: string;
}

// Mock tRPC-like hook for fetching data
const useBorrowingPower = () => {
  return useQuery<BorrowingPowerData, ErrorResponse>({
    queryKey: ['borrowingPower'],
    queryFn: async () => {
      // Simulate API call delay
      await new Promise(resolve => setTimeout(resolve, 1000));
      // Simulate success or error
      if (Math.random() > 0.1) { // 90% success rate
        return {
          totalCollateral: 10000,
          totalBorrowed: 3000,
          borrowLimit: 7000,
          availableBorrowPower: 4000,
          healthFactor: 2.3,
        };
      } else {
        throw new Error('Failed to fetch borrowing power data');
      }
    },
    staleTime: 5 * 60 * 1000, // Data considered fresh for 5 minutes
    retry: 2, // Retry failed requests 2 times
  });
};

const BorrowingPowerScreen: React.FC = () => {
  const { data, isLoading, isError, error, refetch } = useBorrowingPower();
  const [isDarkTheme, setIsDarkTheme] = useState(false);

  useEffect(() => {
    document.documentElement.classList.toggle('dark', isDarkTheme);
  }, [isDarkTheme]);

  const getHealthFactorColor = useCallback((healthFactor: number) => {
    if (healthFactor > 2) return 'text-green-500';
    if (healthFactor > 1.5) return 'text-yellow-500';
    return 'text-red-500';
  }, []);

  if (isLoading) {
    return (
      <div className="flex items-center justify-center min-h-screen bg-gray-100 dark:bg-gray-900">
        <p className="text-lg text-gray-700 dark:text-gray-300">Loading borrowing power data...</p>
      </div>
    );
  }

  if (isError) {
    return (
      <div className="flex flex-col items-center justify-center min-h-screen bg-gray-100 dark:bg-gray-900 p-4">
        <Card className="w-full max-w-md">
          <CardHeader>
            <CardTitle className="text-red-500">Error</CardTitle>
          </CardHeader>
          <CardContent>
            <p className="text-gray-700 dark:text-gray-300">Failed to load borrowing power: {error?.message || 'Unknown error'}</p>
            <Button onClick={() => refetch()} className="mt-4">Retry</Button>
          </CardContent>
        </Card>
      </div>
    );
  }

  const borrowingPowerPercentage = data ? (data.totalBorrowed / data.borrowLimit) * 100 : 0;

  return (
    <div className="min-h-screen bg-gray-100 dark:bg-gray-900 text-gray-900 dark:text-gray-100 p-4 sm:p-6 lg:p-8 transition-colors duration-200">
      <div className="max-w-4xl mx-auto space-y-6" role="region" aria-labelledby="borrowing-power-title">
        <div className="flex justify-between items-center">
          <h1 id="borrowing-power-title" className="text-3xl font-bold">Crypto: Borrowing Power</h1>
          <div className="flex items-center space-x-2">
            <Switch
              id="dark-mode-switch"
              checked={isDarkTheme}
              onCheckedChange={setIsDarkTheme}
              aria-label="Toggle dark mode"
            />
            <Label htmlFor="dark-mode-switch">Dark Mode</Label>
          </div>
        </div>

        <Card className="shadow-lg">
          <CardHeader>
            <CardTitle>Overview</CardTitle>
          </CardHeader>
          <CardContent className="grid grid-cols-1 md:grid-cols-2 gap-4">
            <div>
              <p className="text-sm text-gray-500 dark:text-gray-400">Total Collateral</p>
              <p className="text-2xl font-semibold">${data?.totalCollateral.toLocaleString()}</p>
            </div>
            <div>
              <p className="text-sm text-gray-500 dark:text-gray-400">Total Borrowed</p>
              <p className="text-2xl font-semibold">${data?.totalBorrowed.toLocaleString()}</p>
            </div>
            <div>
              <p className="text-sm text-gray-500 dark:text-gray-400">Borrow Limit</p>
              <p className="text-2xl font-semibold">${data?.borrowLimit.toLocaleString()}</p>
            </div>
            <div>
              <p className="text-sm text-gray-500 dark:text-gray-400">Available Borrow Power</p>
              <p className="text-2xl font-semibold">${data?.availableBorrowPower.toLocaleString()}</p>
            </div>
          </CardContent>
        </Card>

        <Card className="shadow-lg">
          <CardHeader>
            <CardTitle>Health Factor</CardTitle>
          </CardHeader>
          <CardContent>
            <p className={`text-4xl font-bold ${getHealthFactorColor(data?.healthFactor || 0)}`} aria-live="polite">
              {data?.healthFactor.toFixed(2)}
            </p>
            <p className="text-sm text-gray-500 dark:text-gray-400 mt-2">
              Your health factor indicates the safety of your collateral. Higher is better.
            </p>
          </CardContent>
        </Card>

        <Card className="shadow-lg">
          <CardHeader>
            <CardTitle>Borrowing Power Usage</CardTitle>
          </CardHeader>
          <CardContent>
            <Progress value={borrowingPowerPercentage} className="w-full" aria-label="Borrowing power usage" />
            <p className="text-sm text-gray-500 dark:text-gray-400 mt-2">
              {borrowingPowerPercentage.toFixed(2)}% of your borrowing limit used.
            </p>
          </CardContent>
        </Card>

        <div className="flex justify-center">
          <Button onClick={() => refetch()} aria-label="Refresh borrowing power data">
            Refresh Data
          </Button>
        </div>
      </div>
    </div>
  );
};

export default BorrowingPowerScreen;
