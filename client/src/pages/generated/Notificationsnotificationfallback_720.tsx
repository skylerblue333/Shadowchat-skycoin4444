// Mock tRPC hook for demonstration purposes
// In a real application, this would be generated by tRPC
const useGetNotifications = () => {
  const [data, setData] = React.useState<string[] | null>(null);
  const [isLoading, setIsLoading] = React.useState(true);
  const [isError, setIsError] = React.useState(false);

  React.useEffect(() => {
    const timer = setTimeout(() => {
      // Simulate API call success or failure
      const success = Math.random() > 0.5;
      if (success) {
        setData([]); // No notifications for fallback scenario
      } else {
        setIsError(true);
      }
      setIsLoading(false);
    }, 1500);
    return () => clearTimeout(timer);
  }, []);

  return { data, isLoading, isError, refetch: () => {} }; // refetch for demonstration
};

interface NotificationsNotificationFallbackProps {
  title?: string;
  description?: string;
  onRefresh?: () => void;
}

const NotificationsNotificationFallback: React.FC<any> = ({
  title = 'No Notifications',
  description = 'You currently have no new notifications. We\'ll let you know when something new happens.',
  onRefresh,
}) => {
  const { data, isLoading, isError, refetch } = useGetNotifications();

  const handleRefresh = () => {
    refetch();
    onRefresh?.();
  };

  if (isLoading) {
    return (
      <div className="flex flex-col items-center justify-center h-full p-4 space-y-4">
        <Skeleton className="h-12 w-12 rounded-full" />
        <Skeleton className="h-4 w-[250px]" />
        <Skeleton className="h-4 w-[200px]" />
        <Loader2 className="h-8 w-8 animate-spin text-gray-400 dark:text-gray-500" aria-label="Loading notifications" />
      </div>
    );
  }

  if (isError) {
    return (
      <div className="p-4">
        <Alert variant="destructive" role="alert" aria-live="assertive">
          <TriangleAlert className="h-4 w-4" />
          <AlertTitle>Error</AlertTitle>
          <AlertDescription>
            Failed to load notifications. Please try again.
          </AlertDescription>
          <Button onClick={handleRefresh} className="mt-4" aria-label="Refresh notifications">
            Retry
          </Button>
        </Alert>
      </div>
    );
  }

  // Assuming data is an empty array for the fallback scenario
  if (data && data.length === 0) {
    return (
      <div className="flex flex-col items-center justify-center h-full p-4 text-center text-gray-500 dark:text-gray-400">
        <Terminal className="h-16 w-16 mb-4 text-gray-400 dark:text-gray-500" aria-hidden="true" />
        <h2 className="text-2xl font-bold tracking-tight mb-2" id="no-notifications-title">
          {title}
        </h2>
        <p className="text-sm text-gray-600 dark:text-gray-300 mb-4" aria-labelledby="no-notifications-title">
          {description}
        </p>
        <Button onClick={handleRefresh} aria-label="Check for new notifications">
          Check for new notifications
        </Button>
      </div>
    );
  }

  return null; // Should not reach here in a fallback scenario if data is empty
};

export default NotificationsNotificationFallback;
