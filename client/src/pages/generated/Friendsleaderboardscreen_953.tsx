// Define the interface for a leaderboard entry
interface LeaderboardEntry {
  id: string;
  rank: number;
  name: string;
  score: number;
  isCurrentUser: boolean;
}

// Mock tRPC-like hook for fetching data
// In a real application, this would be generated by tRPC and interact with a backend API.
const useFriendsLeaderboard = () => {
  return useQuery<LeaderboardEntry[], Error>({
    queryKey: ['friendsLeaderboard'],
    queryFn: async () => {
      // Simulate API call delay
      await new Promise(resolve => setTimeout(resolve, 1000));
      // Mock data
      return [
        { id: '1', rank: 1, name: 'Alice', score: 1500, isCurrentUser: false },
        { id: '2', rank: 2, name: 'Bob', score: 1450, isCurrentUser: false },
        { id: '3', rank: 3, name: 'You', score: 1400, isCurrentUser: true },
        { id: '4', rank: 4, name: 'Charlie', score: 1300, isCurrentUser: false },
        { id: '5', rank: 5, name: 'David', score: 1250, isCurrentUser: false },
      ];
    },
    // Example of error handling and retry logic
    retry: 3,
    retryDelay: 1000,
  });
};

const FriendsLeaderboardScreen: React.FC = () => {
  const { data: leaderboard, isLoading, isError, error, refetch } = useFriendsLeaderboard();
  const [isDarkMode, setIsDarkMode] = useState(false);

  // Toggle dark mode class on body for demonstration
  useEffect(() => {
    if (isDarkMode) {
      document.documentElement.classList.add('dark');
    } else {
      document.documentElement.classList.remove('dark');
    }
  }, [isDarkMode]);

  if (isError) {
    return (
      <div className="flex items-center justify-center min-h-screen bg-background text-foreground p-4">
        <Card className="w-full max-w-md">
          <CardHeader>
            <CardTitle>Error Loading Leaderboard</CardTitle>
          </CardHeader>
          <CardContent>
            <p className="text-red-500">Failed to load friends leaderboard: {error?.message}</p>
            <Button onClick={() => refetch()} className="mt-4">Retry</Button>
          </CardContent>
        </Card>
      </div>
    );
  }

  return (
    <div className={cn(
      "min-h-screen bg-background text-foreground p-4 md:p-8 lg:p-12",
      isDarkMode ? "dark" : ""
    )}>
      <div className="max-w-4xl mx-auto">
        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-2xl font-bold">Friends Leaderboard</CardTitle>
            <Button
              variant="outline"
              size="sm"
              onClick={() => setIsDarkMode(!isDarkMode)}
              aria-label="Toggle dark mode"
            >
              {isDarkMode ? "Light Mode" : "Dark Mode"}
            </Button>
          </CardHeader>
          <CardContent>
            {isLoading ? (
              <div className="space-y-3">
                {[...Array(5)].map((_, i) => (
                  <Skeleton key={i} className="h-10 w-full" />
                ))}
              </div>
            ) : (
              <Table>
                <TableHeader>
                  <TableRow>
                    <TableHead className="w-[80px]">Rank</TableHead>
                    <TableHead>Name</TableHead>
                    <TableHead className="text-right">Score</TableHead>
                  </TableRow>
                </TableHeader>
                <TableBody>
                  {leaderboard?.map((entry) => (
                    <TableRow key={entry.id} className={cn({'bg-primary/10': entry.isCurrentUser})}>
                      <TableCell className="font-medium">{entry.rank}</TableCell>
                      <TableCell>{entry.name}</TableCell>
                      <TableCell className="text-right">{entry.score}</TableCell>
                    </TableRow>
                  ))}
                </TableBody>
              </Table>
            )}
          </CardContent>
        </Card>
      </div>
    </div>
  );
};

export default FriendsLeaderboardScreen;
