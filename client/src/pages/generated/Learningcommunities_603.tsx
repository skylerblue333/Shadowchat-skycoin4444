// Placeholder for tRPC hook. In a real app, this would be generated by tRPC.

interface Community {
  id: string;
  name: string;
  description: string;
  members: number;
  topics: string[];
}

export const LearningCommunities: React.FC = () => {
  const [searchTerm, setSearchTerm] = useState('');
  const { data: communities, isLoading, isError, error } = useStubQuery();

  const filteredCommunities = useMemo(() => {
    if (!communities) return [];
    return communities.filter(community =>
      community.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
      community.description.toLowerCase().includes(searchTerm.toLowerCase()) ||
      community.topics.some(topic => topic.toLowerCase().includes(searchTerm.toLowerCase()))
    );
  }, [communities, searchTerm]);

  if (isLoading) {
    return (
      <div className="p-4 space-y-6 dark:bg-gray-900 min-h-screen" aria-live="polite" aria-busy="true">
        <h1 className="text-4xl font-extrabold tracking-tight lg:text-5xl dark:text-white">Learning Communities</h1>
        <div className="relative">
          <Search className="absolute left-2 top-3 h-4 w-4 text-gray-500 dark:text-gray-400" />
          <Input
            type="text"
            placeholder="Search communities..."
            className="pl-8 pr-4 py-2 border rounded-md w-full dark:bg-gray-800 dark:border-gray-700 dark:text-white"
            disabled
          />
        </div>
        <div className="grid gap-6 md:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4">
          {[1, 2, 3, 4, 5].map((i) => (
            <Card key={i} className="dark:bg-gray-800 dark:border-gray-700 shadow-lg">
              <CardHeader>
                <Skeleton className="h-6 w-3/4 mb-2 bg-gray-300 dark:bg-gray-700" />
                <Skeleton className="h-4 w-1/2 bg-gray-300 dark:bg-gray-700" />
              </CardHeader>
              <CardContent className="space-y-2">
                <Skeleton className="h-4 w-full bg-gray-300 dark:bg-gray-700" />
                <Skeleton className="h-4 w-5/6 bg-gray-300 dark:bg-gray-700" />
                <Skeleton className="h-4 w-2/3 bg-gray-300 dark:bg-gray-700" />
              </CardContent>
            </Card>
          ))}
        </div>
      </div>
    );
  }

  if (isError) {
    return (
      <div className="p-4 space-y-6 dark:bg-gray-900 min-h-screen" role="alert">
        <h1 className="text-4xl font-extrabold tracking-tight lg:text-5xl dark:text-white">Learning Communities</h1>
        <Alert variant="destructive" className="dark:bg-red-900 dark:border-red-700 dark:text-white">
          <Terminal className="h-5 w-5" />
          <AlertTitle>Error Loading Communities</AlertTitle>
          <AlertDescription>
            Failed to load learning communities: {error?.message || 'An unexpected error occurred. Please try again later.'}
          </AlertDescription>
        </Alert>
      </div>
    );
  }

  return (
    <div className="p-4 space-y-6 dark:bg-gray-900 min-h-screen" aria-live="polite">
      <h1 className="text-4xl font-extrabold tracking-tight lg:text-5xl dark:text-white">Learning Communities</h1>
      <div className="relative">
        <Search className="absolute left-2 top-3 h-4 w-4 text-gray-500 dark:text-gray-400" />
        <Input
          type="text"
          placeholder="Search communities by name, description, or topics..."
          className="pl-8 pr-4 py-2 border rounded-md w-full dark:bg-gray-800 dark:border-gray-700 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent"
          value={searchTerm}
          onChange={(e) => setSearchTerm(e.target.value)}
          aria-label="Search learning communities"
        />
      </div>
      <div className="grid gap-6 md:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4">
        {filteredCommunities.length > 0 ? (
          filteredCommunities.map((community) => (
            <Card key={community.id} className="dark:bg-gray-800 dark:border-gray-700 shadow-lg hover:shadow-xl transition-shadow duration-200">
              <CardHeader>
                <CardTitle className="text-xl font-semibold dark:text-white">{community.name}</CardTitle>
                <CardDescription className="text-sm dark:text-gray-300">{community.description}</CardDescription>
              </CardHeader>
              <CardContent className="space-y-2 text-sm dark:text-gray-400">
                <p><strong>Members:</strong> {community.members.toLocaleString()}</p>
                <p><strong>Topics:</strong> {community.topics.join(', ')}</p>
                {/* Add more details or actions here */}
              </CardContent>
            </Card>
          ))
        ) : (
          <p className="text-gray-600 dark:text-gray-400 col-span-full">No communities found matching your search.</p>
        )}
      </div>
    </div>
  );
};

export default LearningCommunities;
