// This component displays the risk score for a given cryptocurrency.
// It utilizes tRPC for data fetching, shadcn/ui for styling, and React 19 features.
// The component includes loading states, error handling, and dark theme support.

// Mock tRPC hook for demonstration purposes.
// In a real application, this would be generated by your tRPC setup.

interface CryptoRiskScoreProps {
  /**
   * The ID of the cryptocurrency for which to display the risk score.
   * Example: 'SKYCOIN4444'
   */
  cryptoId: string;
}

const CryptoRiskScore: React.FC<any> = ({ cryptoId }) => {
  // Fetch the risk score using the mocked tRPC hook.
  const { data: riskScore, isLoading, isError, error } = useStubQuery(cryptoId);

  // Display a loading skeleton while data is being fetched.
  if (isLoading) {
    return (
      <div className="p-4 space-y-4 dark:bg-gray-900 min-h-screen flex flex-col items-center justify-center" aria-live="polite" aria-atomic="true">
        <Card className="w-full max-w-md dark:bg-gray-800 dark:text-gray-100">
          <CardHeader>
            <CardTitle className="text-xl">Crypto: Risk Score</CardTitle>
          </CardHeader>
          <CardContent className="flex flex-col items-center space-y-3">
            <Skeleton className="h-8 w-3/4 dark:bg-gray-700" />
            <Skeleton className="h-4 w-1/2 dark:bg-gray-700" />
            <Skeleton className="h-12 w-full dark:bg-gray-700" />
          </CardContent>
        </Card>
      </div>
    );
  }

  // Display an error message if data fetching fails.
  if (isError) {
    return (
      <div className="p-4 dark:bg-gray-900 min-h-screen flex flex-col items-center justify-center" role="alert">
        <Alert variant="destructive" className="w-full max-w-md dark:bg-red-900 dark:text-red-100">
          <ExclamationTriangleIcon className="h-4 w-4" />
          <AlertTitle>Error</AlertTitle>
          <AlertDescription>
            {error?.message || 'An unknown error occurred while fetching the risk score.'}
          </AlertDescription>
        </Alert>
      </div>
    );
  }

  // Helper function to determine the color of the risk score based on its value.
  const getRiskColor = (score: number): string => {
    if (score < 30) return 'text-green-500'; // Low risk
    if (score < 70) return 'text-yellow-500'; // Medium risk
    return 'text-red-500'; // High risk
  };

  return (
    <div className="p-4 dark:bg-gray-900 min-h-screen flex flex-col items-center justify-center">
      <Card className="w-full max-w-md dark:bg-gray-800 dark:text-gray-100">
        <CardHeader>
          <CardTitle className="text-xl">Crypto: Risk Score for {cryptoId}</CardTitle>
        </CardHeader>
        <CardContent className="text-center">
          <p className="text-sm text-gray-500 dark:text-gray-400 mb-2">Current Risk Score:</p>
          <p className={`text-6xl font-bold ${getRiskColor(riskScore!)}`} aria-label={`Risk score is ${riskScore}`}>
            {riskScore}
          </p>
          <p className="text-sm text-gray-500 dark:text-gray-400 mt-4">
            A higher score indicates higher risk. This score is calculated based on various market factors and historical data.
          </p>
        </CardContent>
      </Card>
    </div>
  );
};

export default CryptoRiskScore;
