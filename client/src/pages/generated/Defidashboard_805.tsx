// Mock tRPC hooks for demonstration. In a real app, these would be generated by tRPC.

interface Asset {
  id: string;
  name: string;
  balance: number;
  valueUSD: number;
  change24h: number;
}

interface MarketData {
  time: string;
  price: number;
}

const mockFetchAssets = async (): Promise<Asset[]> => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve([
        { id: 'eth', name: 'Ethereum', balance: 2.5, valueUSD: 7500, change24h: 5.2 },
        { id: 'btc', name: 'Bitcoin', balance: 0.1, valueUSD: 6800, change24h: -1.5 },
        { id: 'sol', name: 'Solana', balance: 15, valueUSD: 2250, change24h: 10.1 },
      ]);
    }, 1000);
  });
};

const mockFetchMarketData = async (): Promise<MarketData[]> => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve([
        { time: '00:00', price: 3000 },
        { time: '04:00', price: 3100 },
        { time: '08:00', price: 3050 },
        { time: '12:00', price: 3200 },
        { time: '16:00', price: 3150 },
        { time: '20:00', price: 3300 },
        { time: '24:00', price: 3250 },
      ]);
    }, 1500);
  });
};

const useAssets = () => trpc.useQuery<Asset[], Error>('assets', mockFetchAssets);
const useMarketData = () => trpc.useQuery<MarketData[], Error>('marketData', mockFetchMarketData);

const DeFiDashboard: React.FC = () => {
  const { data: assets, isLoading: assetsLoading, error: assetsError } = useAssets();
  const { data: marketData, isLoading: marketDataLoading, error: marketDataError } = useMarketData();
  const [isDarkTheme, setIsDarkTheme] = useState(false);

  useEffect(() => {
    document.documentElement.classList.toggle('dark', isDarkTheme);
  }, [isDarkTheme]);

  if (assetsError || marketDataError) {
    return <div className="flex items-center justify-center min-h-screen text-red-500">Error loading data. Please try again.</div>;
  }

  return (
    <div className="min-h-screen bg-background text-foreground p-4 md:p-8 lg:p-12 transition-colors duration-300">
      <div className="max-w-7xl mx-auto space-y-8">
        <header className="flex justify-between items-center mb-8">
          <h1 className="text-4xl font-bold">DeFi Dashboard</h1>
          <div className="flex items-center space-x-2">
            <Switch
              id="dark-mode"
              checked={isDarkTheme}
              onCheckedChange={setIsDarkTheme}
              aria-label="Toggle dark mode"
            />
            <Label htmlFor="dark-mode">Dark Mode</Label>
          </div>
        </header>

        <section className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          <Card className="col-span-1 md:col-span-2 lg:col-span-1">
            <CardHeader>
              <CardTitle>Total Portfolio Value</CardTitle>
            </CardHeader>
            <CardContent>
              {assetsLoading ? (
                <div className="h-24 flex items-center justify-center">Loading...</div>
              ) : (
                <p className="text-5xl font-bold">${assets?.reduce((sum, asset) => sum + asset.valueUSD, 0).toFixed(2)}</p>
              )}
            </CardContent>
          </Card>

          <Card>
            <CardHeader>
              <CardTitle>Assets</CardTitle>
            </CardHeader>
            <CardContent>
              {assetsLoading ? (
                <div className="h-48 flex items-center justify-center">Loading assets...</div>
              ) : (
                <ul className="space-y-2">
                  {assets?.map((asset) => (
                    <li key={asset.id} className="flex justify-between items-center">
                      <span>{asset.name} ({asset.balance.toFixed(2)})</span>
                      <span className={`${asset.change24h >= 0 ? 'text-green-500' : 'text-red-500'}`}>
                        ${asset.valueUSD.toFixed(2)} ({asset.change24h.toFixed(2)}%)
                      </span>
                    </li>
                  ))}
                </ul>
              )}
            </CardContent>
          </Card>

          <Card className="lg:col-span-1">
            <CardHeader>
              <CardTitle>Market Overview</CardTitle>
            </CardHeader>
            <CardContent>
              {marketDataLoading ? (
                <div className="h-48 flex items-center justify-center">Loading market data...</div>
              ) : (
                <ResponsiveContainer width="100%" height={200}>
                  <LineChart data={marketData}>
                    <CartesianGrid strokeDasharray="3 3" stroke="hsl(var(--border))" />
                    <XAxis dataKey="time" stroke="hsl(var(--foreground))" />
                    <YAxis stroke="hsl(var(--foreground))" />
                    <Tooltip contentStyle={{ backgroundColor: 'hsl(var(--background))', border: '1px solid hsl(var(--border))' }} />
                    <Line type="monotone" dataKey="price" stroke="hsl(var(--primary))" strokeWidth={2} dot={false} />
                  </LineChart>
                </ResponsiveContainer>
              )}
            </CardContent>
          </Card>
        </section>

        <section className="grid grid-cols-1 md:grid-cols-2 gap-6">
          <Card>
            <CardHeader>
              <CardTitle>Send Funds</CardTitle>
            </CardHeader>
            <CardContent className="space-y-4">
              <div>
                <Label htmlFor="recipient">Recipient Address</Label>
                <Input id="recipient" placeholder="0x..." />
              </div>
              <div>
                <Label htmlFor="amount">Amount</Label>
                <Input id="amount" type="number" placeholder="0.00" />
              </div>
              <Button className="w-full">Send</Button>
            </CardContent>
          </Card>

          <Card>
            <CardHeader>
              <CardTitle>Transaction History</CardTitle>
            </CardHeader>
            <CardContent>
              {assetsLoading ? (
                <div className="h-48 flex items-center justify-center">Loading transactions...</div>
              ) : (
                <p className="text-muted-foreground">No recent transactions.</p>
              )}
            </CardContent>
          </Card>
        </section>
      </div>
    </div>
  );
};

export default DeFiDashboard;
