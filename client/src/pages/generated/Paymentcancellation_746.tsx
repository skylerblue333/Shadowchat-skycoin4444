// Simulate tRPC hook for payment cancellation
// In a real application, this would be generated by tRPC client

interface PaymentCancellationProps {
  paymentId: string;
  onCancellationSuccess?: () => void;
  onCancellationError?: (error: string) => void;
}

export function PaymentCancellation({ paymentId, onCancellationSuccess, onCancellationError }: PaymentCancellationProps) {
  const [cancellationReason, setCancellationReason] = useState('');
  const [isDialogOpen, setIsDialogOpen] = useState(false);
  const { mutate, isLoading, error } = useStubMutation();

  const handleCancelPayment = async () => {
    if (!cancellationReason.trim()) {
      alert('Please provide a reason for cancellation.');
      return;
    }

    const result = await mutate(cancellationReason);
    if (result.success) {
      setIsDialogOpen(false);
      onCancellationSuccess?.();
    } else {
      onCancellationError?.(result.error || 'Unknown error');
    }
  };

  return (
    <Card className="w-[350px] dark:bg-gray-900 dark:text-gray-50">
      <CardHeader>
        <CardTitle>Cancel Payment</CardTitle>
        <CardDescription>Are you sure you want to cancel payment <span className="font-bold">{paymentId}</span>?</CardDescription>
      </CardHeader>
      <CardContent>
        <form onSubmit={(e) => e.preventDefault()}>
          <div className="grid w-full items-center gap-4">
            <div className="flex flex-col space-y-1.5">
              <Label htmlFor="reason">Reason for Cancellation</Label>
              <Input
                id="reason"
                placeholder="e.g., Duplicate order, customer request"
                value={cancellationReason}
                onChange={(e) => setCancellationReason(e.target.value)}
                aria-describedby="reason-description"
                className="dark:bg-gray-800 dark:text-gray-50 dark:border-gray-700"
              />
              <p id="reason-description" className="text-sm text-gray-500 dark:text-gray-400">This reason will be recorded.</p>
            </div>
            {error && <p className="text-red-500 text-sm dark:text-red-400" role="alert">Error: {error}</p>}
          </div>
        </form>
        <Dialog open={isDialogOpen} onOpenChange={setIsDialogOpen}>
          <DialogTrigger asChild>
            <Button
              variant="destructive"
              className="mt-4 w-full dark:bg-red-600 dark:hover:bg-red-700 dark:text-white"
              disabled={isLoading}
              aria-controls="cancel-payment-dialog"
            >
              {isLoading ? 'Cancelling...' : 'Confirm Cancellation'}
            </Button>
          </DialogTrigger>
          <DialogContent className="sm:max-w-[425px] dark:bg-gray-900 dark:text-gray-50">
            <DialogHeader>
              <DialogTitle>Confirm Payment Cancellation</DialogTitle>
              <DialogDescription>
                This action cannot be undone. Are you absolutely sure you want to cancel payment <span className="font-bold">{paymentId}</span>?
              </DialogDescription>
            </DialogHeader>
            <DialogFooter>
              <Button variant="outline" onClick={() => setIsDialogOpen(false)} className="dark:border-gray-700 dark:text-gray-50 dark:hover:bg-gray-800">
                Go Back
              </Button>
              <Button
                variant="destructive"
                onClick={handleCancelPayment}
                disabled={isLoading}
                className="dark:bg-red-600 dark:hover:bg-red-700 dark:text-white"
              >
                {isLoading ? 'Cancelling...' : 'Yes, Cancel Payment'}
              </Button>
            </DialogFooter>
          </DialogContent>
        </Dialog>
      </CardContent>
    </Card>
  );
}


