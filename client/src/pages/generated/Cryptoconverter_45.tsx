// Mock tRPC hooks for demonstration. In a real app, these would be generated by tRPC.

// Define form schema using Zod
const formSchema = z.object({
  fromCurrency: z.string().min(1, { message: 'Please select a currency.' }),
  toCurrency: z.string().min(1, { message: 'Please select a currency.' }),
  amount: z.number().positive({ message: 'Amount must be positive.' }),
});

type FormData = z.infer<typeof formSchema>;

const CryptoConverter: React.FC = () => {
  const [convertedAmount, setConvertedAmount] = useState<number | null>(null);
  const [conversionRate, setConversionRate] = useState<number | null>(null);
  const [isDarkTheme, setIsDarkTheme] = useState<boolean>(false);

  const { data: currencies, isLoading: isLoadingCurrencies, error: currenciesError } = useStubQuery();

  const { mutate: convertCrypto, isLoading: isConverting, error: conversionError } = useStubMutation({
    onSuccess: (data: { convertedAmount: number; rate: number }) => {
      setConvertedAmount(data.convertedAmount);
      setConversionRate(data.rate);
    },
    onError: (error: Error) => {
      console.error('Conversion error:', error.message);
      // Optionally, set an error state to display to the user
    },
  });

  const form = useForm<FormData>({
    resolver: zodResolver(formSchema),
    defaultValues: {
      fromCurrency: '',
      toCurrency: '',
      amount: 0,
    },
  });

  const { handleSubmit, formState: { errors } } = form;

  const onSubmit = (data: FormData) => {
    convertCrypto(data);
  };

  useEffect(() => {
    if (isDarkTheme) {
      document.documentElement.classList.add('dark');
    } else {
      document.documentElement.classList.remove('dark');
    }
  }, [isDarkTheme]);

  return (
    <Card className={`w-[350px] mx-auto p-4 ${isDarkTheme ? 'dark bg-gray-900 text-white' : 'bg-white text-gray-900'}`}>
      <CardHeader>
        <CardTitle>Crypto Converter</CardTitle>
      </CardHeader>
      <CardContent>
        {isLoadingCurrencies && <p>Loading currencies...</p>}
        {currenciesError && <p className="text-red-500">Error loading currencies: {currenciesError.message}</p>}

        <form onSubmit={handleSubmit(onSubmit)} className="space-y-4">
          <div>
            <Label htmlFor="fromCurrency">From Currency</Label>
            <Select onValueChange={(value) => form.setValue('fromCurrency', value)} value={form.watch('fromCurrency')}>
              <SelectTrigger>
                <SelectValue placeholder="Select a currency" />
              </SelectTrigger>
              <SelectContent>
                {currencies?.map((currency) => (
                  <SelectItem key={currency.id} value={currency.id}>
                    {currency.name} ({currency.symbol})
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
            {errors.fromCurrency && <p className="text-red-500 text-sm">{errors.fromCurrency.message}</p>}
          </div>

          <div>
            <Label htmlFor="toCurrency">To Currency</Label>
            <Select onValueChange={(value) => form.setValue('toCurrency', value)} value={form.watch('toCurrency')}>
              <SelectTrigger>
                <SelectValue placeholder="Select a currency" />
              </SelectTrigger>
              <SelectContent>
                {currencies?.map((currency) => (
                  <SelectItem key={currency.id} value={currency.id}>
                    {currency.name} ({currency.symbol})
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
            {errors.toCurrency && <p className="text-red-500 text-sm">{errors.toCurrency.message}</p>}
          </div>

          <div>
            <Label htmlFor="amount">Amount</Label>
            <Input
              id="amount"
              type="number"
              {...form.register('amount', { valueAsNumber: true })}
              className={errors.amount ? 'border-red-500' : ''}
            />
            {errors.amount && <p className="text-red-500 text-sm">{errors.amount.message}</p>}
          </div>

          <Button type="submit" disabled={isConverting}>
            {isConverting ? 'Converting...' : 'Convert'}
          </Button>
        </form>

        {conversionError && <p className="text-red-500 mt-4">Error converting: {conversionError.message}</p>}

        {convertedAmount !== null && conversionRate !== null && (
          <div className="mt-4 p-3 border rounded-md">
            <p>Conversion Rate: 1 {form.watch('fromCurrency')} = {conversionRate.toFixed(4)} {form.watch('toCurrency')}</p>
            <p>Converted Amount: {convertedAmount.toFixed(4)} {form.watch('toCurrency')}</p>
          </div>
        )}

        <div className="mt-4 flex items-center space-x-2">
          <input
            type="checkbox"
            id="dark-mode-toggle"
            checked={isDarkTheme}
            onChange={() => setIsDarkTheme(!isDarkTheme)}
            className="h-4 w-4 text-blue-600 focus:ring-blue-500 border-gray-300 rounded"
          />
          <Label htmlFor="dark-mode-toggle">Dark Mode</Label>
        </div>
      </CardContent>
    </Card>
  );
};

export default CryptoConverter;
