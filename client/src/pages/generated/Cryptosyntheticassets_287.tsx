const CryptoSyntheticAssets: React.FC = () => {
  const { data, isLoading, isError, error, refetch } = useSyntheticAssets();
  const [isDarkMode, setIsDarkMode] = useState<boolean>(false);

  useEffect(() => {
    // Simulate dark mode toggle based on system preference or user setting
    const mediaQuery = window.matchMedia('(prefers-color-scheme: dark)');
    setIsDarkMode(mediaQuery.matches);
    const handleChange = (e: MediaQueryListEvent) => setIsDarkMode(e.matches);
    mediaQuery.addEventListener('change', handleChange);
    return () => mediaQuery.removeEventListener('change', handleChange);
  }, []);

  const themeClass = isDarkMode ? 'dark' : 'light';

  return (
    <div className={`min-h-screen bg-background text-foreground p-8 ${themeClass}`}>
      <h1 className="text-4xl font-extrabold mb-8 text-center">Synthetic Assets Overview</h1>

      {isError && (
        <Alert variant="destructive" className="mb-6">
          <AlertTitle>Error</AlertTitle>
          <AlertDescription>{error?.message || data?.error || 'An unknown error occurred.'}</AlertDescription>
        </Alert>
      )}

      {isLoading ? (
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          {[1, 2, 3].map((i) => (
            <Card key={i}>
              <CardHeader>
                <Skeleton className="h-6 w-3/4 mb-2" />
                <Skeleton className="h-4 w-1/2" />
              </CardHeader>
              <CardContent>
                <Skeleton className="h-8 w-full mb-2" />
                <Skeleton className="h-4 w-1/4" />
              </CardContent>
            </Card>
          ))}
        </div>
      ) : (
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          {data?.data?.map((asset) => (
            <Card key={asset.id} className="hover:shadow-lg transition-shadow duration-200 ease-in-out">
              <CardHeader>
                <CardTitle className="text-2xl font-semibold">{asset.name} ({asset.symbol})</CardTitle>
              </CardHeader>
              <CardContent>
                <p className="text-3xl font-bold mb-2">${asset.price.toFixed(2)}</p>
                <p className={`text-lg ${asset.change24h >= 0 ? 'text-green-500' : 'text-red-500'}`}>
                  {asset.change24h >= 0 ? '+' : ''}{asset.change24h.toFixed(2)}%
                </p>
              </CardContent>
            </Card>
          ))}
        </div>
      )}

      {!isLoading && !isError && (!data?.data || data.data.length === 0) && (
        <p className="text-center text-lg text-muted-foreground">No synthetic assets to display.</p>
      )}

      <div className="mt-8 text-center">
        <button
          onClick={() => refetch()}
          className="px-6 py-3 bg-primary text-primary-foreground rounded-md shadow-md hover:bg-primary/90 focus:outline-none focus:ring-2 focus:ring-primary focus:ring-offset-2"
        >
          Refresh Assets
        </button>
      </div>
    </div>
  );
};

export default CryptoSyntheticAssets;
