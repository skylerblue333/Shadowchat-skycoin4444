// Mock tRPC hook for demonstration purposes
// In a real application, this would be generated by tRPC
const useCryptoSearch = (query: string) => {
  const [data, setData] = React.useState<CryptoResult[] | null>(null);
  const [isLoading, setIsLoading] = React.useState(true);
  const [error, setError] = React.useState<string | null>(null);

  React.useEffect(() => {
    const fetchData = async () => {
      setIsLoading(true);
      setError(null);
      try {
        // Simulate API call
        await new Promise(resolve => setTimeout(resolve, 1000));
        if (query === 'error') {
          throw new Error('Failed to fetch crypto data.');
        }
        const dummyData: CryptoResult[] = [
          {
            id: 'bitcoin',
            name: 'Bitcoin',
            symbol: 'BTC',
            priceUsd: '60000.00',
            changePercent24Hr: '2.50',
          },
          {
            id: 'ethereum',
            name: 'Ethereum',
            symbol: 'ETH',
            priceUsd: '3000.00',
            changePercent24Hr: '-1.20',
          },
          {
            id: 'ripple',
            name: 'Ripple',
            symbol: 'XRP',
            priceUsd: '0.50',
            changePercent24Hr: '0.75',
          },
          {
            id: 'litecoin',
            name: 'Litecoin',
            symbol: 'LTC',
            priceUsd: '150.00',
            changePercent24Hr: '3.10',
          },
        ];
        setData(dummyData.filter(crypto => crypto.name.toLowerCase().includes(query.toLowerCase())));
      } catch (err: any) {
        setError(err.message);
      } finally {
        setIsLoading(false);
      }
    };

    fetchData();
  }, [query]);

  return { data, isLoading, error };
};

interface CryptoResult {
  id: string;
  name: string;
  symbol: string;
  priceUsd: string;
  changePercent24Hr: string;
}

interface CryptoSearchResultsProps {
  query: string;
}

const CryptoSearchResults: React.FC<any> = ({ query }) => {
  const { data: results, isLoading, error } = useCryptoSearch(query);

  if (isLoading) {
    return (
      <div className="flex items-center justify-center h-screen dark:bg-gray-900" role="status" aria-live="polite">
        <div className="animate-spin rounded-full h-32 w-32 border-t-2 border-b-2 border-blue-500"></div>
        <p className="text-lg text-gray-700 dark:text-gray-300 ml-4">Loading search results for "{query}"...</p>
      </div>
    );
  }

  if (error) {
    return (
      <div className="flex items-center justify-center h-screen dark:bg-gray-900 text-red-500 text-lg" role="alert">
        <p>Error: {error}</p>
      </div>
    );
  }

  if (!results || results.length === 0) {
    return (
      <div className="flex items-center justify-center h-screen dark:bg-gray-900 text-gray-700 dark:text-gray-300 text-lg">
        <p>No results found for "{query}".</p>
      </div>
    );
  }

  return (
    <div className="container mx-auto p-4 dark:bg-gray-900 min-h-screen" aria-label="Crypto Search Results">
      <h1 className="text-3xl font-bold mb-6 text-gray-900 dark:text-white">Search Results for "{query}"</h1>
      <div className="overflow-x-auto">
        <Table className="min-w-full bg-white dark:bg-gray-800 shadow-md rounded-lg overflow-hidden">
          <TableHeader className="bg-gray-200 dark:bg-gray-700">
            <TableRow>
              <TableHead className="py-3 px-4 text-left text-sm font-medium text-gray-600 dark:text-gray-200 uppercase tracking-wider">Name</TableHead>
              <TableHead className="py-3 px-4 text-left text-sm font-medium text-gray-600 dark:text-gray-200 uppercase tracking-wider">Symbol</TableHead>
              <TableHead className="py-3 px-4 text-left text-sm font-medium text-gray-600 dark:text-gray-200 uppercase tracking-wider">Price (USD)</TableHead>
              <TableHead className="py-3 px-4 text-left text-sm font-medium text-gray-600 dark:text-gray-200 uppercase tracking-wider">Change (24Hr)</TableHead>
            </TableRow>
          </TableHeader>
          <TableBody className="divide-y divide-gray-200 dark:divide-gray-700">
            {results.map((crypto) => (
              <TableRow key={crypto.id} className="hover:bg-gray-100 dark:hover:bg-gray-700 transition-colors duration-200">
                <TableCell className="py-3 px-4 whitespace-nowrap text-gray-800 dark:text-gray-200">{crypto.name}</TableCell>
                <TableCell className="py-3 px-4 whitespace-nowrap text-gray-800 dark:text-gray-200">{crypto.symbol}</TableCell>
                <TableCell className="py-3 px-4 whitespace-nowrap text-gray-800 dark:text-gray-200">${parseFloat(crypto.priceUsd).toFixed(2)}</TableCell>
                <TableCell className="py-3 px-4 whitespace-nowrap">
                  <span className={parseFloat(crypto.changePercent24Hr) >= 0 ? 'text-green-500' : 'text-red-500'}>
                    {parseFloat(crypto.changePercent24Hr).toFixed(2)}%
                  </span>
                </TableCell>
              </TableRow>
            ))}
          </TableBody>
        </Table>
      </div>
    </div>
  );
};

export default CryptoSearchResults;
