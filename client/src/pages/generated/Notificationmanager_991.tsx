const NotificationManager: React.FC = () => {
  const { data: fetchedNotifications, isLoading: isFetchingNotifications, error: fetchError } = useGetNotifications();
  const { mutate: updateNotificationStatus, isLoading: isUpdatingNotification } = useUpdateNotification();
  const { mutate: deleteNotificationEntry, isLoading: isDeletingNotification } = useDeleteNotification();

  const [notifications, setNotifications] = useState<Notification[]>([]);
  const [filter, setFilter] = useState<'all' | 'read' | 'unread'>('all');
  const [searchTerm, setSearchTerm] = useState<string>('');

  useEffect(() => {
    if (fetchedNotifications) {
      setNotifications(fetchedNotifications);
    }
  }, [fetchedNotifications]);

  const filteredNotifications = notifications.filter((notification) => {
    const matchesFilter = filter === 'all' || (filter === 'read' && notification.read) || (filter === 'unread' && !notification.read);
    const matchesSearch = notification.message.toLowerCase().includes(searchTerm.toLowerCase());
    return matchesFilter && matchesSearch;
  });

  const toggleReadStatus = async (id: string) => {
    const notificationToUpdate = notifications.find(notif => notif.id === id);
    if (notificationToUpdate) {
      await updateNotificationStatus({ id, read: !notificationToUpdate.read });
      setNotifications((prev) =>
        prev.map((notif) => (notif.id === id ? { ...notif, read: !notif.read } : notif))
      );
    }
  };

  const deleteNotification = async (id: string) => {
    await deleteNotificationEntry({ id });
    setNotifications((prev) => prev.filter((notif) => notif.id !== id));
  };

  if (isFetchingNotifications) {
    return (
      <div className="container mx-auto p-4 text-center">
        <p>Loading notifications...</p>
      </div>
    );
  }

  if (fetchError) {
    return (
      <div className="container mx-auto p-4 text-center text-red-500">
        <p>Error: {fetchError.message}</p>
      </div>
    );
  }

  return (
    <div className="container mx-auto p-4 space-y-6">
      <h1 className="text-4xl font-extrabold mb-8 text-center">Notification Manager</h1>

      <Card className="shadow-lg">
        <CardHeader>
          <CardTitle className="text-2xl">Filter Notifications</CardTitle>
          <CardDescription>Refine your view of notifications by status and keywords.</CardDescription>
        </CardHeader>
        <CardContent className="flex flex-col md:flex-row gap-4 items-center">
          <Input
            placeholder="Search notifications..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            className="flex-grow md:w-auto"
            aria-label="Search notifications"
          />
          <div className="flex gap-2 flex-wrap justify-center">
            <Button
              variant={filter === 'all' ? 'default' : 'outline'}
              onClick={() => setFilter('all')}
              aria-pressed={filter === 'all'}
            >
              All
            </Button>
            <Button
              variant={filter === 'read' ? 'default' : 'outline'}
              onClick={() => setFilter('read')}
              aria-pressed={filter === 'read'}
            >
              Read
            </Button>
            <Button
              variant={filter === 'unread' ? 'default' : 'outline'}
              onClick={() => setFilter('unread')}
              aria-pressed={filter === 'unread'}
            >
              Unread
            </Button>
          </div>
        </CardContent>
      </Card>

      <section aria-labelledby="notification-list-heading">
        <h2 id="notification-list-heading" className="sr-only">Notification List</h2>
        <div className="grid gap-4">
          {filteredNotifications.length === 0 ? (
            <p className="text-center text-gray-500 py-8">No notifications found matching your criteria.</p>
          ) : (
            filteredNotifications.map((notification) => (
              <Card key={notification.id} className={`flex flex-col md:flex-row items-start md:items-center justify-between p-4 shadow-md ${notification.read ? 'opacity-60 bg-gray-50 dark:bg-gray-800' : 'bg-white dark:bg-gray-900'}`}>
                <div className="flex-grow mb-2 md:mb-0">
                  <CardTitle className="text-lg font-semibold">{notification.message}</CardTitle>
                  <CardDescription className="text-sm text-gray-600 dark:text-gray-400">
                    {new Date(notification.timestamp).toLocaleString()} - <span className={`font-medium ${notification.type === 'error' ? 'text-red-600' : notification.type === 'warning' ? 'text-yellow-600' : notification.type === 'success' ? 'text-green-600' : 'text-blue-600'}`}>{notification.type.charAt(0).toUpperCase() + notification.type.slice(1)}</span>
                  </CardDescription>
                </div>
                <div className="flex items-center gap-3">
                  <div className="flex items-center space-x-2">
                    <Switch
                      id={`read-status-${notification.id}`}
                      checked={notification.read}
                      onCheckedChange={() => toggleReadStatus(notification.id)}
                      aria-label={`Mark notification as ${notification.read ? 'unread' : 'read'}`}
                      disabled={isUpdatingNotification}
                    />
                    <label htmlFor={`read-status-${notification.id}`} className="text-sm font-medium leading-none peer-disabled:cursor-not-allowed peer-disabled:opacity-70">
                      {notification.read ? 'Read' : 'Unread'}
                    </label>
                  </div>
                  <Button
                    variant="destructive"
                    size="sm"
                    onClick={() => deleteNotification(notification.id)}
                    disabled={isDeletingNotification}
                    aria-label={`Delete notification: ${notification.message}`}
                  >
                    Delete
                  </Button>
                </div>
              </Card>
            ))
          )}
        </div>
      </section>
    </div>
  );
};

export default NotificationManager;
