// Mock tRPC hook for fetching heatmap data
// In a real application, this would be generated by tRPC

interface HeatmapCellProps {
  name: string;
  value: number;
  change: number;
}

const HeatmapCell: React.FC<any> = ({ name, value, change }) => {
  const bgColor = change > 0 ? 'bg-green-500/20' : 'bg-red-500/20';
  const textColor = change > 0 ? 'text-green-400' : 'text-red-400';

  return (
    <div
      className={cn(
        'flex flex-col items-center justify-center p-2 rounded-md text-sm font-medium',
        bgColor,
        'hover:ring-2 hover:ring-primary transition-all duration-200',
        'dark:bg-opacity-30 dark:hover:ring-offset-background'
      )}
      aria-label={`${name}: Value ${value.toFixed(2)}, Change ${change.toFixed(2)}%`}
    >
      <span className="text-foreground/80 dark:text-foreground/90">{name}</span>
      <span className={cn('text-xs', textColor)}>{change.toFixed(2)}%</span>
    </div>
  );
};

const CryptoHeatmapView: React.FC = () => {
  const { data, isLoading, isError } = useStubQuery();

  if (isLoading) {
    return (
      <div className="p-4 grid grid-cols-5 gap-4">
        {Array.from({ length: 25 }).map((_, i) => (
          <Skeleton key={i} className="h-24 w-full" />
        ))}
      </div>
    );
  }

  if (isError) {
    return (
      <div className="flex items-center justify-center h-64 text-red-500 dark:text-red-400">
        <p className="text-lg font-medium">Error loading heatmap data. Please try again.</p>
      </div>
    );
  }

  return (
    <Card className="w-full max-w-6xl mx-auto dark:bg-gray-900 dark:border-gray-700">
      <CardHeader>
        <CardTitle className="text-2xl font-bold tracking-tight dark:text-white">Crypto Heatmap</CardTitle>
      </CardHeader>
      <CardContent>
        <div className="grid grid-cols-5 md:grid-cols-10 gap-4" role="grid" aria-label="Cryptocurrency Heatmap">
          {data?.map((coin) => (
            <HeatmapCell key={coin.id} name={coin.name} value={coin.value} change={coin.change} />
          ))}
        </div>
      </CardContent>
    </Card>
  );
};

export default CryptoHeatmapView;
