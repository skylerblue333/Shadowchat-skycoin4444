// Define types for prediction market data
interface PredictionMarket {
  id: string;
  question: string;
  options: { id: string; text: string; odds: number }[];
  status: 'open' | 'closed';
  userPrediction?: string;
}

// Simulate tRPC client and hooks
// In a real application, these would be generated by tRPC

// Custom hook to simulate tRPC query
const usePredictionMarketQuery = (marketId: string) => {
  return useQuery<PredictionMarket, Error>({
    queryKey: ['predictionMarket', marketId],
    queryFn: () => trpc.prediction.getMarket(marketId),
  });
};

// Custom hook to simulate tRPC mutation
const useMakePredictionMutation = () => {
  const queryClient = new QueryClient();
  return useMutation<void, Error, { marketId: string; optionId: string }>({
    mutationFn: ({ marketId, optionId }) => trpc.prediction.makePrediction(marketId, optionId),
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['predictionMarket'] });
    },
  });
};

interface CryptoPredictionMarketScreenProps {
  marketId: string;
}

const CryptoPredictionMarketScreen: React.FC<any> = ({ marketId }) => {
  const { data: market, isLoading, isError, error } = usePredictionMarketQuery(marketId);
  const { mutate: makePrediction, isLoading: isPredicting } = useMakePredictionMutation();
  const [selectedOption, setSelectedOption] = useState<string | null>(null);
  const { theme } = useTheme(); // Access current theme

  const handlePredict = () => {
    if (selectedOption && market) {
      makePrediction({ marketId: market.id, optionId: selectedOption });
    }
  };

  if (isLoading) {
    return (
      <div className="flex items-center justify-center min-h-screen bg-background text-foreground">
        <p>Loading prediction market...</p>
      </div>
    );
  }

  if (isError) {
    return (
      <div className="flex items-center justify-center min-h-screen bg-background text-foreground text-red-500">
        <p>Error: {error?.message || 'Failed to load prediction market.'}</p>
      </div>
    );
  }

  if (!market) {
    return (
      <div className="flex items-center justify-center min-h-screen bg-background text-foreground">
        <p>No prediction market found.</p>
      </div>
    );
  }

  return (
    <div className={`min-h-screen flex items-center justify-center p-4 ${theme === 'dark' ? 'dark' : ''}`}>
      <Card className="w-full max-w-md bg-card text-card-foreground shadow-lg rounded-lg">
        <CardHeader>
          <CardTitle className="text-2xl font-bold text-center" aria-label="Prediction Market Question">
            {market.question}
          </CardTitle>
          <CardDescription className="text-center text-muted-foreground">
            Status: {market.status === 'open' ? 'Open for predictions' : 'Closed'}
          </CardDescription>
        </CardHeader>
        <CardContent className="space-y-4">
          <div role="radiogroup" aria-label="Prediction Options">
            {market.options.map((option) => (
              <div key={option.id} className="flex items-center space-x-2 mb-2">
                <Input
                  type="radio"
                  id={option.id}
                  name="predictionOption"
                  value={option.id}
                  checked={selectedOption === option.id}
                  onChange={() => setSelectedOption(option.id)}
                  disabled={market.status === 'closed' || isPredicting}
                  className="form-radio h-4 w-4 text-primary focus:ring-primary border-border"
                  aria-checked={selectedOption === option.id}
                  aria-label={option.text}
                />
                <Label htmlFor={option.id} className="text-lg font-medium cursor-pointer">
                  {option.text} (Odds: {option.odds.toFixed(2)})
                </Label>
              </div>
            ))}
          </div>
          <Button
            onClick={handlePredict}
            disabled={!selectedOption || market.status === 'closed' || isPredicting}
            className="w-full py-2 text-lg font-semibold bg-primary text-primary-foreground hover:bg-primary/90 focus:outline-none focus:ring-2 focus:ring-primary focus:ring-offset-2 focus:ring-offset-background"
            aria-live="polite"
            aria-disabled={!selectedOption || market.status === 'closed' || isPredicting}
          >
            {isPredicting ? 'Submitting...' : 'Make Prediction'}
          </Button>
          {market.userPrediction && (
            <p className="text-center text-sm text-muted-foreground mt-4">
              You predicted: {market.userPrediction}
            </p>
          )}
        </CardContent>
      </Card>
    </div>
  );
};

